namespace _6._1___The_Variable_Shop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //:::::::: integer types ::::::::
            //signed
            int a = -1;
            long b = -2;
            short c = -3;
            sbyte d = -4;

            //unsigned
            uint e = 5;
            ulong f = 6;
            ushort g = 7;
            byte h = 8;

            //:::::::: floating point types ::::::::
            float i = 9.5f;
            double j = 50.100;
            decimal k = 100m;

            char l = 'A';
            bool m = false;

            Console.WriteLine(a);
            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
            Console.WriteLine(g);
            Console.WriteLine(h);
            Console.WriteLine(i);
            Console.WriteLine(j);
            Console.WriteLine(k);
            Console.WriteLine(l);
            Console.WriteLine(m);

        }
    }
}
